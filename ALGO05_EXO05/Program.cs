{
    internal class Program
    {
        static void Main(string[] args)
        {
            int i = 0;
            int j = 0;

            for (i = 1; i < 10; i++)
            {
                for (j = 1; j < 10; j++)
                {
                    Console.WriteLine(i * j);
                }
            }
        }
    }
